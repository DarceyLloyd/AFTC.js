window.stringToBool = function ($string) {
	switch ($string.toLowerCase()) {
		case "y":
			return true;
			break;
		case "yes":
			return true;
			break;
		case "1":
			return true;
			break;
		case "true":
			return true;
			break;
		case "y":
			return true;
			break;
		default:
			return false;
			break;
	}
}
